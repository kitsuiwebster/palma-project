async function createLocationGeoJSONs() {
  console.log('üó∫Ô∏è  Creating individual GeoJSON files for each location...\n');
  
  // Cr√©er le dossier locations
  if (!fs.existsSync(LOCATIONS_DIR)) {
    fs.mkdirSync(LOCATIONS_DIR, { recursive: true });
    console.log(`üìÅ Created directory: ${LOCATIONS_DIR}`);
  }
  
  // Lire le CSV pour obtenir les locations utilis√©es
  const csvContent = fs.readFileSync(LOCATION_CSV, 'utf8');
  const lines = csvContent.split('\n');
  const usedLocations = new Set();
  
  for (let i = 1; i < lines.length; i++) {
    const line = lines[i].trim();
    if (line) {
      const [locationCode] = line.split(',');
      if (locationCode && locationCode !== 'Area_code_L3') {
        usedLocations.add(locationCode);
      }
    }
  }
  
  console.log(`üìä Found ${usedLocations.size} unique locations in CSV`);
  
  let created = 0;
  let skipped = 0;
  let missing = 0;
  
  // Cr√©er un GeoJSON pour chaque location
  for (const locationCode of usedLocations) {
    const filename = `${locationCode.toLowerCase()}.json`;
    const filepath = path.join(LOCATIONS_DIR, filename);
    
    if (fs.existsSync(filepath)) {
      console.log(`‚è≠Ô∏è  ${locationCode}: Already exists`);
      skipped++;
      continue;
    }
    
    const locationInfo = LOCATION_COORDINATES[locationCode];
    if (!locationInfo) {
      console.log(`‚ùå ${locationCode}: No coordinates defined`);
      missing++;
      continue;
    }
    
    const geoJSON = {
      "type": "Feature",
      "properties": {
        "name": locationInfo.name,
        "locationCode": locationCode,
        "speciesCount": 0, // Will be calculated later
        "densityZone": "Palm Distribution Zone",
        "color": "#2d5a2d"
      },
      "geometry": {
        "type": "Polygon",
        "coordinates": [locationInfo.coords]
      }
    };
    
    try {
      fs.writeFileSync(filepath, JSON.stringify(geoJSON, null, 2), 'utf8');
      console.log(`‚úÖ ${locationCode}: Created ${filename}`);
      created++;
    } catch (error) {
      console.log(`‚ùå ${locationCode}: Error creating file - ${error.message}`);
      missing++;
    }
  }
  
  console.log(`\nüéâ Location GeoJSON creation complete!`);
  console.log(`üìä Statistics:`);
  console.log(`   ‚úÖ Created: ${created}`);
  console.log(`   ‚è≠Ô∏è  Skipped (already exist): ${skipped}`);
  console.log(`   ‚ùå Missing coordinates: ${missing}`);
  console.log(`   üìÅ Total files in directory: ${created + skipped}`);
  
  if (missing > 0) {
    console.log(`\n‚ö†Ô∏è  Note: ${missing} locations need coordinate definitions`);
  }
}

